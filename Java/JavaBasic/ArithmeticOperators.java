package Java.JavaBasic;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int n1 = 15;
        int n2 = 4;

        System.out.println("Soma: " + (n1 + n2)); // 19
        System.out.println("Subtração: " + (n1 - n2)); // 11
        System.out.println("Multiplicação: " + (n1 * n2)); // 60
        System.out.println("Divisão Inteira: " + (n1 / n2)); // 3
        System.out.println("Resto da Divisão: " + (n1 % n2)); // 3
    }
}
